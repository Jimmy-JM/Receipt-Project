package Receipt;

public class OrderArr {
	int no;
	String name;
	int price;
	String description;
	int count;

	OrderArr (int no, int price, int count, String description, String name) {
		this.no = no;
		this.name = name;
		this.price = price;
		this.description = description;
		this.count = count;
	}
	
}

